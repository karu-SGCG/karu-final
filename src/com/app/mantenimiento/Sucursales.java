    public void guardar() {
        st.close();
        sessionHibernate();
        st.beginTransaction();
        
        Date fecha = new Date();
        
        MtSucursales s = new MtSucursales();
        s.setCodSucursal(textCodigoSucursal.getText());
        s.setNomSucursal(textNombreSucursal.getText());
        s.setDireccion(textDireccion.getText());
        s.setTelefono(textTelefono.getText());
        s.setMtCiudades((MtCiudades) st.get(MtCiudades.class, Integer.parseInt(textCodCiudad.getText())));
        s.setMtEmpresas((MtEmpresas) st.get(MtEmpresas.class, Integer.parseInt(textCodEmpleado.getText())));
        s.setUsuarioMod(this.usuario);
        s.setFechaMod(fecha);
        
        st.saveOrUpdate(s);
        st.getTransaction().commit();
        st.close();
        getData();
        JOptionPane.showMessageDialog(null, "Registro guardado");
    }


