    private void initValues(MtSucursales s) {
        textCodigoSucursal.setText(s.getCodSucursal());
        textCodigoSucursal.setEnabled(false);

        textCodCiudad.setText(Integer.toString(s.getMtCiudades().getCodCiudad()));
        textCodCiudad.setEnabled(true);

        textNombreSucursal.setText(s.getNomSucursal());
        textNombreSucursal.setEnabled(true);

        textCodEmpleado.setText(String.valueOf(s.getMtEmpresas().getCodEmp()));
        textCodEmpleado.setEnabled(true);

        textDireccion.setText(s.getDireccion());
        textDireccion.setEnabled(true);

        textTelefono.setText(s.getTelefono());
        textTelefono.setEnabled(true);

        botonGuardar.setEnabled(true);
    }


