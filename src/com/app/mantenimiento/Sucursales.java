    private void getData() {
        sessionHibernate();
        sucursales = new ArrayList<MtSucursales>();
        st.beginTransaction();
        String q = "select * from mt_sucursales;";
        query = st.createSQLQuery(q);
        query.addEntity(MtSucursales.class);
        sucursales = query.list();
        st.getTransaction().commit();
        String[][] data = new String[sucursales.size()][6];

        for (int i = 0; i < sucursales.size(); i++) {
            data[i][0] = sucursales.get(i).getCodSucursal();
            data[i][1] = sucursales.get(i).getNomSucursal();
            data[i][2] = sucursales.get(i).getDireccion();
            data[i][3] = sucursales.get(i).getTelefono();
            data[i][4] = Integer.toString(sucursales.get(i).getMtCiudades().getCodCiudad());
            data[i][5] = Integer.toString(sucursales.get(i).getMtEmpresas().getCodEmp());
        }

        String[] columnNames = {"Codigo", "Nombre Sucursal", "Direccion", "Telefono", "Codigo Ciudad", "Codigo Empleado"};
        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        TablaListaSucursales.setModel(model);
        TablaListaSucursales.setAutoscrolls(true);

    }


