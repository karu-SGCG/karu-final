    private void informe() {

        JDialog viewer = new JDialog(this, "Informe Sucursales", true);
        viewer.setSize(800, 600);
        viewer.setLocationRelativeTo(null);

        try {
            String fileName = "src/com/informe/informe_sucursales.jrxml";
            File theFile = new File(fileName);
            JasperDesign jasperDesign = JRXmlLoader.load(theFile);

            //Build a new query
            String theQuery = "select cod_sucursal, nom_sucursal as sucursal,  "
                    + "direccion, telefono, mt_empresas.nom_emp as empresa, "
                    + "mt_ciudades.nom_ciudad as ciudad from mt_sucursales, "
                    + "mt_empresas, mt_ciudades "
                    + "where mt_sucursales.cod_emp = mt_empresas.cod_emp "
                    + "and mt_sucursales.cod_ciudad = mt_ciudades.cod_ciudad;";

            // update the data query
            JRDesignQuery newQuery = new JRDesignQuery();
            newQuery.setText(theQuery);
            jasperDesign.setQuery(newQuery);

            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
            Connection conn = st.connection();
            System.out.println(conn.toString());
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, conn);
            JasperViewer.viewReport(jasperPrint);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
