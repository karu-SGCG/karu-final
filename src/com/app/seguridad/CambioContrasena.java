    private void cmd_cambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_cambiarActionPerformed
        validarCamposVacios();
        Date fecha = new Date();
        if (this.txt_loginContrasenaActual.getText().equals(this.getContrasena())) {
            if (this.txt_loginContrasenaNueva.getText().equals(this.txt_loginCambioContrasenaRep.getText())) {
                try {
                    MtUsuarios user = (MtUsuarios) st.load(MtUsuarios.class, this.getUsuario());
                    user.setContasenia(this.txt_loginContrasenaNueva.getText());
                    user.setUsuarioMod(usuario);
                    user.setFechaMod(fecha);
                    user.setAccionMod("Editar");
                    st.beginTransaction();
                    st.update(user);
                    st.getTransaction().commit();
                    JOptionPane.showMessageDialog(null, "Contraseña cambiada satisfactoriamente.",
                            "Cambio de Contraseña", JOptionPane.INFORMATION_MESSAGE);
                    arranque();
                    this.cmd_cancelar.setText("Cerrar");
                } catch (HibernateException | HeadlessException e) {
                    st.beginTransaction().rollback();
                    JOptionPane.showMessageDialog(null, "Se ha producido un error al intentar cambiar contraseña",
                            "Cambio de Contraseña", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "La nueva contrasena ingresada no coincide. Verificar datos",
                        "Cambio de Contraseña", JOptionPane.ERROR_MESSAGE);
                this.txt_loginContrasenaNueva.grabFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "La contraseña actual no corresponde con la ingresada. Intente de nuevo.",
                    "Cambio de Contraseña", JOptionPane.ERROR_MESSAGE);
            this.txt_loginContrasenaActual.grabFocus();
        }
    }//GEN-LAST:event_cmd_cambiarActionPerformed

