    public void validarCamposVacios() {
        if (this.txt_loginCambioContrasenaRep.getText().isEmpty()
                || this.txt_loginContrasenaActual.getText().isEmpty()
                || this.txt_loginContrasenaNueva.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Existen campos vacíos. Verificar datos.",
                    "Cambio de Contraseña", JOptionPane.ERROR_MESSAGE);
            this.txt_loginContrasenaActual.grabFocus();
        }
    }

    public void arranque() {
        this.txt_loginCambioContrasenaRep.setText("");
        this.txt_loginContrasenaNueva.setText("");
        this.txt_loginContrasenaActual.setText("");
    }

